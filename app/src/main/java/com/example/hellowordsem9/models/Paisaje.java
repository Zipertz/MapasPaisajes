package com.example.hellowordsem9.models;

public class Paisaje {
    public int id;
    public String titulo;

    public String img;
    public double latitude;
    public double longitude;

    public Paisaje() {
        this.id = id;
        this.titulo = titulo;
        this.img = img;
        this.latitude = latitude;
        this.longitude = longitude;
    }

}